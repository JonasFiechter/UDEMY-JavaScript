const day = 'Thursday'

switch (day) {
    case 'Monday':
        console.log('today is monday')
        break
    case 'Tuesday':
        console.log('today is Tuesday')
        break
    case 'Wednesday':
        console.log('today is Wednsday')
        break
    case 'Thursday':
        console.log('today is Thursday')
        break
    default:
        console.log('not a valid day...')
}
